#include "3-calc.h"

/**
* op_add - addition
* @a: number 1
* @b: number 2
* Return: sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - substract
* @a: number 1
* @b: number 2
* Return: sub
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiplicate
* @a: number 1
* @b: number 2
* Return: mul
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - divide
* @a: number 1
* @b: number 2
* Return: div
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - module
* @a: number 1
* @b: number 2
* Return: mod
*/
int op_mod(int a, int b)
{
	return (a % b);
}
